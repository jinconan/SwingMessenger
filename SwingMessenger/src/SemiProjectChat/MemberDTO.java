package SemiProjectChat;

public class MemberDTO {
	String   MEM_ID         = null;
	String   MEM_PW         = null;
	int      MEM_NO         = 0;
	String   MEM_NAME       = null;
	String   MEM_HP         = null;
	String   MEM_HIREDATE   = null;
	
	public final String getMEM_ID() {
		return MEM_ID;
	}
	public final void setMEM_ID(String mEM_ID) {
		this.MEM_ID = mEM_ID;
	}
	public final String getMEM_PW() {
		return MEM_PW;
	}
	public final void setMEM_PW(String mEM_PW) {
		this.MEM_PW = mEM_PW;
	}
	public final int getMEM_NO() {
		return MEM_NO;
	}
	public final void setMEM_NO(int mEM_NO) {
		this.MEM_NO = mEM_NO;
	}
	public final String getMEM_NAME() {
		return MEM_NAME;
	}
	public final void setMEM_NAME(String mEM_NAME) {
		this.MEM_NAME = mEM_NAME;
	}
	public final String getMEM_HP() {
		return MEM_HP;
	}
	public final void setMEM_HP(String mEM_HP) {
		this.MEM_HP = mEM_HP;
	}
	public final String getMEM_HIREDATE() {
		return MEM_HIREDATE;
	}
	public final void setMEM_HIREDATE(String mEM_HIREDATE) {
		this.MEM_HIREDATE = mEM_HIREDATE;
	}
}
