package SemiProjectChat;

public class TalkDTO {

	 int    TALK_NO    = 0;
	 String TALK_NAME  = null;
	 int    TALK_CODE  = 0;
	 
	public final int getTALK_NO() {
		return TALK_NO;
	}
	public final void setTALK_NO(int tALK_NO) {
		this.TALK_NO = tALK_NO;
	}
	public final String getTALK_NAME() {
		return TALK_NAME;
	}
	public final void setTALK_NAME(String tALK_NAME) {
		this.TALK_NAME = tALK_NAME;
	}
	public final int getTALK_CODE() {
		return TALK_CODE;
	}
	public final void setTALK_CODE(int tALK_CODE) {
		TALK_CODE = tALK_CODE;
	}
	
}
